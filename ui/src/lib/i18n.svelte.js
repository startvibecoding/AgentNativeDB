// 简单的 i18n 实现
export function createI18n(initialLocale = 'zh') {
  let _locale = $state(initialLocale);

  return {
    get locale() { return _locale; },
    set locale(v) { _locale = v; },
    t(key) {
      return messages[_locale]?.[key] ?? messages.zh[key] ?? key;
    },
    toggle() {
      _locale = _locale === 'zh' ? 'en' : 'zh';
    },
  };
}
